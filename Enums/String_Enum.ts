// String Enum 

enum Color {
	red = "RED",
	green = "GREEN",
	blue = "BLUE",
	yellow = "YELLOW",
	orange = "ORANGE",
	purple = "PURPLE",
}

console.log(Color.red); // RED
console.log(Color.green); // GREEN
console.log(Color.blue); // BLUE
console.log(Color.yellow); // YELLOW
console.log(Color.orange); // ORANGE

// Array in TypeScript

const StringArray: string[] = ["Hello", "My Dear", "Friend"];
const NumberArray: number[] = [1, 2, 3, 4, 5];
const BooleanArray: boolean[] = [true, false, true, false];


console.log(StringArray);
console.log(NumberArray);
console.log(BooleanArray);
console.log(StringArray[0]); // Hello
console.log(NumberArray[1]); // 2
console.log(BooleanArray[2]); // true
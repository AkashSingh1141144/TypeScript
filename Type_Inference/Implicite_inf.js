// Implicite inference
// TypeScript automatically infers the type based on the assigned value, without any type annotations.
var x = 10;
var y = "Hello";
var z = true;
console.log("x: ".concat(x, ", type: ").concat(typeof x));
console.log("y: ".concat(y, ", type: ").concat(typeof y));
console.log("z: ".concat(z, ", type: ").concat(typeof z));

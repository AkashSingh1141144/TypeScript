// Tuple in TypeScript
// Tuple is a special type of array that can hold a fixed number of elements of different types. It is useful when you know the number of elements and their types in advance.
var person = ["Akash", 21, true];
var person2 = ["Sandhya", 20, false];
console.log("Person: ".concat(person));
console.log("Person2: ".concat(person2));
console.log("Person Name: ".concat(person[0].toUpperCase()));
console.log("Person Age: ".concat(person[1]));
var person3 = ["Akash", 21, true];
var person4 = ["Sandhya", 20, false];
var person5 = ["Sakshi", 22, true];
var person6 = ["Madhuri", 21, true];
console.log("Person3: ".concat(person3));
console.log("Person4: ".concat(person4));
console.log("Person5: ".concat(person5));
console.log("Person6: ".concat(person6));

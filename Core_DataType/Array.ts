// Array Data Type in TypeScript

const userNames : string[] = ["Akash", "Kumar", "Singh"];
const numbers : number[] = [1, 2, 3, 4, 5];
const mixedArray : (string | number)[] = ["Akash", 1, "Kumar", 2, "Singh", 3];
const booleanArray : boolean[] = [true, false, true, false];

console.log(`User Names Array: ${userNames}`);
console.log(`Numbers Array: ${numbers}, Mixed Array: ${mixedArray}, Boolean Array: ${booleanArray}`);
